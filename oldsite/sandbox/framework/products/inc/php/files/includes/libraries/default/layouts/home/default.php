	<p>This is the default view for the website.  Its view name is named HomeView.  Its file is named home.php and its layout directory is named Home.  Any associated model will be named HomeModel.  </p> 
 	<p>Wondering how to make your own view?  Let's take a look at the naming conventions mentioned above.</p>
	<ul>
		<li><strong>View Name:</strong> This is what your view will be called (e.g. Home, Login, Users).  It is best to have a single word name.</li>
		<li><strong>View Class Name:</strong> This will be the view's name with the word View attached to the end.  The first letter must be capitalized.</li>
		<li><strong>View File Name: </strong>This will be the view's name in all lowercase letters with a '.php' extension. It will reside in the site's views directory.</li>
		<li><strong>View Layout Directory: </strong>This will be the view's name in all lowercase letters.  It will reside in the site's layouts directory.</li>
		<li><strong>View Model Name:</strong>  This will be the view name capitalized with the Word Model attached to the end.  The first letter must be capitalized.</li>
	</ul>
